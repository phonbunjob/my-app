function LoveFortune({ onBack }) {
  const [selectedNumber, setSelectedNumber] = useState(null)
  const [isShaking, setIsShaking] = useState(false)
  const [status, setStatus] = useState('single') // 'single' or 'couple'

  const handleNumberClick = (num) => {
    setIsShaking(true)
    setTimeout(() => {
      setIsShaking(false)
      setSelectedNumber(num)
    }, 500)
  }

  const closeModal = () => {
    setSelectedNumber(null)
  }

  return (
    <div className="love-app">
      {/* Rose petals animation */}
      <div className="rose-petals">
        <div className="petal"></div>
        <div className="petal"></div>
        <div className="petal"></div>
        <div className="petal"></div>
        <div className="petal"></div>
        <div className="petal"></div>
        <div className="petal"></div>
        <div className="petal"></div>
      </div>

      <div className="love-container">
        <button className="back-button" onClick={onBack}>
          <svg width="20" height="20" viewBox="0 0 20 20" fill="none">
            <path d="M12 16L6 10L12 4" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/>
          </svg>
          กลับ
        </button>

        <div className="love-header">
          <div className="header-decoration left"></div>
          <div className="header-content">
            <h1 className="love-title">เซียมซีความรัก</h1>
            <p className="love-tagline">พยากรณ์ดวงความรักของคุณ</p>
          </div>
          <div className="header-decoration right"></div>
        </div>
        
        {/* ปุ่มเลือกสถานะ */}
        <div className="status-selector">
          <button 
            className={`status-btn ${status === 'single' ? 'active' : ''}`}
            onClick={() => setStatus('single')}
          >
            <span className="status-icon">◈</span>
            <span>คนโสด</span>
          </button>
          <button 
            className={`status-btn ${status === 'couple' ? 'active' : ''}`}
            onClick={() => setStatus('couple')}
          >
            <span className="status-icon">◉</span>
            <span>มีคู่แล้ว</span>
          </button>
        </div>

        <p className="love-instruction">เลือกหมายเลขเพื่อดูดวงความรัก</p>

        {/* กรอบเซียมซี */}
        <div className={`love-fortune-box ${isShaking ? 'shaking' : ''}`}>
          <div className="love-numbers-grid">
            {Array.from({ length: 28 }, (_, i) => i + 1).map(num => (
              <button
                key={num}
                className="love-number-button"
                onClick={() => handleNumberClick(num)}
              >
                <span className="number">{num}</span>
                <div className="number-glow"></div>
              </button>
            ))}
          </div>
        </div>
      </div>

      {selectedNumber && (
        <div className="love-modal-overlay" onClick={closeModal}>
          <div className="love-modal-content" onClick={(e) => e.stopPropagation()}>
            <button className="modal-close" onClick={closeModal}>
              <svg width="24" height="24" viewBox="0 0 24 24" fill="none">
                <path d="M18 6L6 18M6 6L18 18" stroke="currentColor" strokeWidth="2" strokeLinecap="round"/>
              </svg>
            </button>
            
            <div className="modal-decoration">
              <div className="decoration-line"></div>
              <div className="decoration-center">✦</div>
              <div className="decoration-line"></div>
            </div>
            
            <h2 className="fortune-title">{loveFortunes[selectedNumber].title}</h2>
            
            <div className="fortune-status">
              {status === 'single' ? 'สำหรับคนโสด' : 'สำหรับคนมีคู่'}
            </div>
            
            <div className="fortune-content">
              <p className="fortune-text">
                {status === 'single' 
                  ? loveFortunes[selectedNumber].single 
                  : loveFortunes[selectedNumber].couple
                }
              </p>
            </div>
            
            <div className="modal-footer">
              <div className="blessing-text">
                ขอให้ความรักของคุณสมหวังดั่งใจ
              </div>
            </div>
          </div>
        </div>
      )}
    </div>
  )
}

export default LoveFortune